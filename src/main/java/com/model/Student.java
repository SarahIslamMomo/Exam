package com.model;

public class Student {
    private int studentRolla;
    private String studentNamea;
    private int studentRollb;
    private String studentNameb;

    public Student() {
    }

    public int getStudentRolla() {
        return studentRolla;
    }

    public void setStudentRolla(int studentRolla) {
        this.studentRolla = studentRolla;
    }

    public String getStudentNamea() {
        return studentNamea;
    }

    public void setStudentNamea(String studentNamea) {
        this.studentNamea = studentNamea;
    }

    public int getStudentRollb() {
        return studentRollb;
    }

    public void setStudentRollb(int studentRollb) {
        this.studentRollb = studentRollb;
    }

    public String getStudentNameb() {
        return studentNameb;
    }

    public void setStudentNameb(String studentNameb) {
        this.studentNameb = studentNameb;
    }
}
